function filter($value)
{
    $One = trim($value);
    $Two = strip_tags($One);
    $Three = htmlspecialchars($Two, ENT_QUOTES);
    $result = $Three;
    return $result;
}

function select($tablo_Adi)
{
    global $db;

    $sorgu = $db->prepare('SELECT * FROM ' . $tablo_Adi);
    $sorgu->execute();
    $sonuc = $sorgu->fetchAll(PDO::FETCH_ASSOC);

    if ($sorgu->rowCount() > 0) {

        return $sonuc[0];
    } else {
        return false;
    }
}

function rowCount($tablo_Adi)
{
    global $db;
    $sorgu = $db->prepare('SELECT * FROM ' . $tablo_Adi);
    $sorgu->execute();

    return $sorgu->rowCount();
}


function GetIP()
{
    if (!empty($_SERVER['HTTP_CLIENT_IP'])) {
        $ip = $_SERVER['HTTP_CLIENT_IP'];
    } elseif (!empty($_SERVER['HTTP_X_FORWARDED_FOR'])) {
        $ip = $_SERVER['HTTP_X_FORWARDED_FOR'];
    } else {
        $ip = $_SERVER['REMOTE_ADDR'];
    }

    return $ip;
}


